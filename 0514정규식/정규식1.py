"""
정규 표현식은 왜 필요한가?
다음과 같은 문제가 주어졌다고 가정해 보자.

주민등록번호를 포함하고 있는 텍스트가 있다. 이 텍스트에 포함된 모든 주민등록번호의 뒷자리를 * 문자로 변경해 보자.
먼저 정규식을 전혀 모르면 다음과 같은 순서로 프로그램을 작성해야 할 것이다.

1.전체 텍스트를 공백 문자로 나눈다(split).
2.나뉜 단어가 주민등록번호 형식인지 조사한다.
3.단어가 주민등록번호 형식이라면 뒷자리를 *로 변환한다.
4.나뉜 단어를 다시 조립한다.
이를 구현한 코드는 아마도 다음과 같을 것이다.
"""

data = """
park 800905-1049118
Life is edd.
kim  700905-1059119
Lee 701005-2059120
"""

result = []
#데이터 분리:
# data.split("\n"): 데이터를 줄 단위로 분리합니다. 이는 각 줄을 리스트의 요소로 만듭니다.
# line.split(" "): 각line을 "공백"기준으로 단어 단위로 split나눠.

for line in data.split("\n"):
    word_result = []
    for word in line.split(" "):
        print(word)

        #길이 및 숫자 체크
        # len(word) == 14: 단어의 길이가 14자리인지 확인합니다.
        # word[0:6] 앞의 6자리(0~6전까지)숫자중.
        #  isdigit(): 숫자냐. -있으면 안됨
        # & word[7:0] &뒤 7번째~끝까지가
        #  .isdigit() 숫자로이뤄졌냐
        
        # .word[6] =='-' -조건을 추가할수도 있다.
        if len(word) == 14 and word[:6].isdigit() and word[7:].isdigit():
            word = word[:6] + "-" + "*******"
            #앞0~6직전까지의 숫자 앞6자리 +  -  + ******
            #문자열가공:조건만족시 고쳐. 앞 6자리 + "-" + "*******"으로 변환

        word_result.append(word)
        #같은라인에 여러개의 주민번호가 있을수있음

    result.append(" ".join(word_result))
    #(word_result)같은라인 주민번호를 "공백"으로 join연결시켜 리스트에 
    #.append 더해서
    #result.에 값을 넣어라.
print("\n".join(result))
"""전체 흐름
1.데이터를 줄 단위로 분리합니다.
2.각 줄을 단어 단위로 분리합니다.
3.각 단어에 대해 조건을 체크하고 조건을 만족하면 가공합니다.
4.가공된 단어를 리스트에 추가합니다.
5.하지만 이 코드에서는 각줄의 결과를 저장안하니, 단순히 가공된 단어를 출력하거나 다른 방식으로 처리해야 합니다.
"""

#반면, 정규식을 사용하면 다음처럼 훨씬 간편하고 직관적인 코드를 작성할 수 있다. 
#아직 정규식 사용 방법을 배우지 않았으므로 눈으로만 살펴보자.
import re 

data = """
park 800905-1049118
kim  700905-1059119
"""

pat = re.compile("(\d{6})[-]\d{7}")  #\d숫자 {6}자리 [-] \d{7}
print(pat.sub("\g<1>-*******", data))

"""
정규식의 기본 개념
정규식은 다음과 같은 기본적인 요소들로 구성됩니다:

문자 클래스: 특정 문자를 쉽게 나타낼 수 있는 방법입니다.
\w: 문자나 숫자 (a-z, A-Z, 0-9)
\W: 문자나 숫자가 아닌 것
\d: 숫자 (0-9)
\D: 숫자가 아닌 것
\s: 공백 문자 (space, tab, newline)
\S: 공백 문자가 아닌 것
반복 지정자: 문자가 얼마나 반복되는지 나타냅니다.
*: 0번 이상 반복
+: 1번 이상 반복
?: 0번 또는 1번 반복
{n}: 정확히 n번 반복
{n, m}: 최소 n번에서 최대 m번 반복
메타 문자: 특수한 의미를 가진 문자입니다.
.: 줄바꿈을 제외한 모든 문자
^: 문자열의 시작
$: 문자열의 끝
|: OR 연산자
(, ): 그룹화
"""
