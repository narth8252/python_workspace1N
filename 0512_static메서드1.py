#1.공통위 데이터 공간을 두고
class Calculator:
    def __init__(self, x=0, y=0): #공통위 데이터공간 (공공데이터)
        self.x = x 
        self.y = y
    def add(self):
        return self.x+self.y
    def sub(self):
        return self.x+self.y
#2.객체만들기
c1 = Calcilator(4, 5)
print(c1.add()) #+
print(c1.sub()) #-

#1-2.공통의 데이터없이 변수만들기
class Calculator2:
    def add(self, x, y):  # ← self 꼭 필요함
        return x + y

    def sub(self, x, y):
        return x - y

c1 = Calculator2()
print("인스턴스 방식 add:", c1.add(4, 5))  # 9
print("인스턴스 방식 sub:", c1.sub(4, 5))  # -1

#클래스가 번잡스러워보일때 self없이 만드는 법이 static메서드
# 정적 메서드 방식 (self 없이 사용)
class Calculator3:
    @staticmethod # @ :데코레이터라고 부름(java는 어노테이션이라고 부름)
    def add(x, y):
        return x + y

    @staticmethod
    def sub(x, y):
        return x - y
    
    #cls를 사용하던말던 매개변수전달은 첫변수는 무조건mul(cls,,)
    @classmethod
    def mul(cls, x, y):
        return x * y

print("정적 메서드 add:", Calculator3.add(4, 5) )  # 9
print("정적 메서드 sub:", Calculator3.sub(4, 5) )  # -1
print("클래식 메서드 sub:", Calculator3.mul(4, 5) )  #20

