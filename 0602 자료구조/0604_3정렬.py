def selectSort2(arr):
    for i in range(0, len(arr)-1):
        min = arr[i] 
        pos = i 
        for j in range(i+1, len(arr)):
            if min > arr[j]:
                min = arr[j]
                pos = j 
        arr[pos], arr[i] = arr[i], arr[pos]  
        print(arr) #과정보기위한 출력
